                         // Arithmatics Operators
#include <iostream>
using namespace std;

int main (){
    int a = 5;
    int b = 4;

    cout << "+" << (a+b) << endl; //9
    cout << "-" << (a-b) << endl; //1
    cout << "*" << (a*b) << endl; //20
    cout << "/" << (a/b) << endl; //1
    cout << "%" << (a%b) << endl; //1
    return 0;
}