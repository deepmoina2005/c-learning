                                            //Assignment Operators
#include <iostream>
using namespace std;

int main (){
    int a = 10;

    a += 5; // a = a + 5 => 15
    cout << a << endl;
    a -= 5; // a = a - 5 => 5
    cout << a << endl; //5
    a *= 5;
    cout << a << endl; //50
    a /= 5;
    cout << a << endl; //2
    
    return 0;
}                                            